      <h3>6.6.  Sécurité des données collectées</h3>
      <p>
       IFDC-BENIN s’engage à prendre toutes mesures afin d’assurer la sécurité et la confidentialité des données à caractère personnel et notamment à empêcher qu’elles ne soient endommagées, effacées ou que des tiers non autorisés y aient accès.</p>
      <p>
        Nous améliorons sans cesse nos procédures de sécurité au fur et à mesure de l’évolution des technologies afin de maintenir un niveau de protection maximum. Notre personnel et celui de nos sous-traitants ayant accès aux données personnelles sont soumis contractuellement à une obligation de confidentialité.</p>
      <p>Par ailleurs, en cas d’incident de sécurité affectant vos Données à caractère personnel (destruction, perte, altération ou divulgation), IFDC-BENIN assure respecter l’obligation de notification des violations de Données à caractère personnel, notamment à l’Autorité de Protection des Données Personnelles (APDP).</p>

    <section class="cgu-section" id="article-6">
      <h2>6.    RESPECT DES EXIGENCES LIEES A LA PROTECTION DES DONNEES A CARACTERE PERSONNEL</h2>
      <h3>6.1.   Privacy by design</h3>
      <p>
        Les services digitaux de IFDC-BENIN intègrent en amont des projets, la protection des données à caractère personnel.
      </p>
      <p>
       IFDC-BENIN s’engage à prendre en compte la protection de vos Données à caractère personnel et de votre vie privée dès la conception des services qui vous sont proposés permettant ainsi de minimiser les risques d’un non-respect du traitement des données à caractère personnel. </p>
      <p>
        Ainsi, des mesures techniques et organisationnelles appropriées au traitement des données à caractère personnel sont prises au regard de la finalité recherchée par IFDC-BENIN dans le traitement envisagé.</p>
      <p>
        L’application de ce principe permet donc de mettre en œuvre des mesures préventives permettant de limiter les risques à l’égard des données à caractère personnel. 
      </p>

      <h3>6.2.  Privacy by default</h3>
      <p>
        Les services digitaux de IFDC-BENIN assurent par défaut, le plus haut niveau de protection des données à caractère personnel.
      </p>
      <p>
       IFDC-BENIN met en œuvre des mesures techniques et organisationnelles appropriées de manière à garantir que par défaut, seules les données à caractère personnel nécessaires à la finalité poursuivie sont traitées.</p>
      

      <h3>6.3.  Finalités des traitements</h3>
      <p>
        IFDC-BENIN s’engage à ne collecter que des données strictement nécessaires à la réalisation des services proposés à savoir :
      </p>
      <p>
        <ul>
            <li>L’inscription et l’établissement de cartes des acteurs du secteur agricole ;</li>
            <li>La collecte de données relatives au prix du marché ;</li>
            <li>La diffusion des prix des informations relatives au marché et aux cultures agroalimentaires (météo, calendrier, paramètres, intrants améliorés etc.)</li>
            <li>La gestion des accès aux services de création et d’analyses de données ;</li>
            <li>La gestion des accès aux données contenues dans les bases de données de IFDC-BENIN ; </li>
            <li>La gestion des crédits garantis par IFDC-BENIN auprès des structures financières décentralisées ;</li>
            <li>La formation continue des acteurs.</li>
            <li> L’élaboration de ratios de fréquence par l’exploitation des cookies des fins statistiques ou d'étude en vue de connaître la fréquentation du site et des pages ainsi que les informations techniques de l'appareil (marque, modèle, plateforme, version) pour les statistiques du backoffice.</li>
        </ul>
       </p>
      <p>
        Par ailleurs, les données à caractère personnel sont utilisées pour proposer des services supplémentaires aux adhérents, uniquement s’ils ont accepté de recevoir de telles communications.
      </p>


      <h3>6.4.  Licéité des traitements</h3>
      <p>
        IFDC-BENIN s’appuie sur les bases légitimes suivantes afin de pouvoir traiter les données à caractère personnel :</p>
      
      <ul>
        <h3>6.4.1.     Exécution du contrat</h3>
        <p>La base légale du traitement des données à caractère personnel de l’utilisateur qui sont collectées est l’exécution de :</p>
        <ul>
            <li>  l’Accord de Partenariat entre la Fédération des Unions des Producteurs du Bénin (FUPRO) et IFDC-BENIN. </li>
            <li>l’Accord de partenariat entre l’Union Nationale des Caisses Rurales d’Epargne et de Prêt (UNACREP) et IFDC-BENIN.</li>
        </ul>
        <p>À cet égard, l’utilisateur est nécessairement tenu de fournir les données nécessaires à leur exécution. S’il ne fournit pas ces données, IFDC-BENIN ne seront pas tenues responsables de la non-exécution du contrat.</p>
      </ul>

      <ul>
        <h3>6.4.2.     Obligations légales </h3>
        <p>Le respect de la lutte contre le blanchiment des capitaux s’impose à IFDC-BENIN. Dans ce contexte, le traitement, la conservation et la transmission à l’Autorité compétente le cas échéant, de vos données à caractère personnel se conforment aux obligations légales et règlementaires et leur permettent de remplir leurs obligations en la matière.</p>
        </ul>

        <ul>
        <h3>6.4.3.     Consentement</h3>
        <p>Pour ce qui est des fins suivantes, la base légale du traitement des données à caractère personnel de l’utilisateur sera le consentement de ce dernier, dans le cas où il l’aurait donné :</p>
        <ul>
            <li>Gestion de l’inscription sur les espaces (extranet) adhérent ;</li>
            <li>Gestion de l’inscription sur les applications mobiles ;</li>
            <li>Gestion de la participation aux sondages ;</li>
            <li>Réponse à l’exercice des droits d'accès, de modification, d'annulation, de limitation et d'opposition, ainsi qu’aux questions et réclamations de l’utilisateur ;</li>
            <li>Gestion de newsletter ou d’autres formes de messages automatisés.</li>
        </ul>
        <p>Le retrait du consentement de ces traitements de la part de l’utilisateur ne conditionnera pas l'exécution des accords conclus par la personne concernée directement avec IFDC-BENIN ou par l’intermédiaire d’une personne morale dans le cadre d’un contrat collectif obligatoire ou facultatif.</p>
      </ul>

     

        <ul>
        <h3>6.4.4.     L’intérêt légitime</h3>
        <p>Pour ce qui est des fins suivantes, la base légale du traitement des Données à caractère personnel de l’utilisateur sera l’intérêt légitime de IFDC-BENIN :</p>
        <ul>
            <li> L’élaboration de profils au moyen de la navigation sur les services digitaux via les données fournies par les utilisateurs inscrits sur celui-ci, ainsi qu’à partir des données fournies par les utilisateurs à toute autre fin.</li>
            <li>L’élaboration de profils pour l’envoi d’informations personnalisées adaptées au profil de l’utilisateur.</li>
            <li>L’envoi d'enquêtes de satisfaction sur les programmes mis en œuvre par IFDC-BENIN afin de leur demander leur avis et de les améliorer.</li>
        </ul>
        <p>La sécurité des services digitaux donne un intérêt légitime à la collecte et permet ainsi de comprendre les besoins, les attentes et le niveau de satisfaction des utilisateurs afin d’améliorer les services, les et produits. Toutes ces actions sont réalisées dans le but d’améliorer le niveau de satisfaction du client et d’assurer une expérience unique de navigation.</p>
      </ul>

